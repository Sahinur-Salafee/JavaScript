// String Methods

let a = "My country name is Bangladesh";

// Includes Method
console.log(a.includes("Bangladesh")); // true

// StartsWith Method
console.log(a.startsWith("My")); // true

// EndsWith Method
console.log(a.endsWith("Bangladesh")); // true

// Repeat Method
console.log("Hello ".repeat(5)); // Hello Hello Hello Hello Hello
console.log(`I want to say  ${'Alhamdulillah\n'.repeat(10)}`);

// Trim Method
let b = "   Hello World   ";
console.log(b.trim()); // Hello World

